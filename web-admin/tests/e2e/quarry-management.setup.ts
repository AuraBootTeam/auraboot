/**
 * Navigate to a menu item by clicking through the sidebar menu hierarchy.
 *
 * @param page - Playwright page
 * @param menuPath - Array of menu labels to click, e.g. ['矿山运营', '数据上报']
 */
export async function navigateToMenu(page: Page, menuPath: string[]): Promise<void> {
  // Ensure we are on a page where the sidebar is visible
  await page.goto(`/`);
  await page.waitForLoadState('domcontentloaded');

  for (let i = 0; i < menuPath.length; i++) {
    const label = menuPath[i];
    const isLast = i === menuPath.length - 1;

    // For parent menus (directories), click to expand
    // For leaf menus, click to navigate
    const menuItem = page
      .locator(`nav >> text="${label}"`)
      .first()
      .or(page.getByText(label, { exact: false }).first());

    await menuItem.waitFor({ state: 'visible', timeout: 10000 });
    await menuItem.click();

    if (isLast) {
      // Wait for navigation to complete
      await page.waitForLoadState('domcontentloaded');
    } else {
      // Wait for submenu expansion animation
      await page.waitForLoadState('domcontentloaded');
    }
  }
}
