// ---------------------------------------------------------------------------
// Shared Test Project — created once per test run
// ---------------------------------------------------------------------------

let _testProjectId: string | null = null;

/**
 * Get or create a shared test project for quarry E2E tests.
 * The project is created once per worker and reused across tests.
 */
export async function getTestProjectId(page: Page): Promise<string> {
  if (_testProjectId) return _testProjectId;

  const result = await executeCommandViaApi(page, 'pm:create_project', {
    pm_project_name: `E2E Quarry Project ${uniqueId()}`,
    pm_project_code: `QE2E-${uniqueId()}`,
  });
  if (result.code !== ErrorCodes.SUCCESS) {
    throw new Error(`Failed to create test project: ${JSON.stringify(result)}`);
  }
  _testProjectId = result.recordId;
  return _testProjectId;
}

