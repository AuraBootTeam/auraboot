-- Subject Permission Group Logic Type Consistency Check
CREATE OR REPLACE FUNCTION check_group_logic_type_consistency()
RETURNS TRIGGER AS $$
DECLARE
    existing_logic_type TEXT;
BEGIN
    SELECT DISTINCT group_logic_type INTO existing_logic_type
    FROM ab_subject_permission
    WHERE tenant_id = NEW.tenant_id
      AND subject_type = NEW.subject_type
      AND subject_id = NEW.subject_id
      AND logic_group = NEW.logic_group
      AND deleted_flag = FALSE
    LIMIT 1;

    IF existing_logic_type IS NOT NULL
       AND existing_logic_type != NEW.group_logic_type THEN
        RAISE EXCEPTION
            'group_logic_type must be consistent within same logic_group. Expected: %, Got: %',
            existing_logic_type, NEW.group_logic_type;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_check_group_logic_type_consistency') THEN
        CREATE TRIGGER trg_check_group_logic_type_consistency
            BEFORE INSERT OR UPDATE ON ab_subject_permission
            FOR EACH ROW
            EXECUTE FUNCTION check_group_logic_type_consistency();
    END IF;
END $$;


