-- ============================================================================================
-- Part B: grant the NEW-code permission to every role that currently grants the OLD code.
-- For each active grant of an old code, insert the mapped new-code grant for the SAME role/tenant,
-- carrying the same grant_type so the access is preserved exactly. Joins are tenant-scoped on both
-- the old and new permission rows so a grant is never created across tenants.
-- ============================================================================================

-- Helper shape (repeated per mapping): join the role's grant of the OLD permission to the tenant's NEW
-- permission row (created in Part A), and insert a grant of the NEW permission for that role.

-- report.definition.view  <- grants of meta.template.read
INSERT INTO ab_role_permission (pid, tenant_id, role_id, permission_id, grant_type, status, deleted_flag, created_at, updated_at)
SELECT 'b6rp' || rp.id || '_v', rp.tenant_id, rp.role_id, np.id, rp.grant_type, 'active', FALSE, NOW(), NOW()
FROM ab_role_permission rp
JOIN ab_permission op ON op.id = rp.permission_id AND op.code = 'meta.template.read'
JOIN ab_permission np ON np.tenant_id = rp.tenant_id AND np.code = 'report.definition.view'
WHERE rp.deleted_flag = FALSE
  AND rp.status = 'active'
  AND NOT EXISTS (
        SELECT 1 FROM ab_role_permission ex
        WHERE ex.tenant_id = rp.tenant_id
          AND ex.role_id = rp.role_id
          AND ex.permission_id = np.id
          AND ex.deleted_flag = FALSE);

