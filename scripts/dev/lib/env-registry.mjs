#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const command = process.argv[2] || 'help';
const options = parseArgs(process.argv.slice(3));

function parseArgs(args) {
  const parsed = {};
  for (let i = 0; i < args.length; i += 1) {
    const arg = args[i];
    if (!arg.startsWith('--')) {
      throw new Error(`unknown argument: ${arg}`);
    }
    const eq = arg.indexOf('=');
    if (eq > -1) {
      parsed[arg.slice(2, eq)] = arg.slice(eq + 1);
      continue;
    }
    const key = arg.slice(2);
    const next = args[i + 1];
    if (next && !next.startsWith('--')) {
      parsed[key] = next;
      i += 1;
    } else {
      parsed[key] = '1';
    }
  }
  return parsed;
}

function usage() {
  console.log(`Usage: env-registry.mjs <upsert|list|inspect|assert-ports-free> [options]

Options:
  --registry-root <path>
  --slug <name>
  --mode <name>
  --product <oss|enterprise>
  --core-root <path>
  --enterprise-root <path>
  --core-branch <name>
  --enterprise-branch <name>
  --compose-project <name>
  --stack-env-file <path>
  --status <running|stopped|adopted>
  --pg-port <port>
  --redis-port <port>
  --be-port <port>
  --vite-port <port>
  --bff-port <port>
  --minio-api-port <port>
  --minio-console-port <port>
  --prod-frontend-port <port>`);
}

function registryRoot() {
  const root = options['registry-root'] || process.env.AURA_ENV_REGISTRY_ROOT;
  if (!root) {
    throw new Error('missing --registry-root or AURA_ENV_REGISTRY_ROOT');
  }
  return path.resolve(root);
}

function registryPath(root) {
  return path.join(root, 'env-registry.json');
}

function envRoot(root, slug) {
  return path.join(root, 'envs', slug);
}

function readJson(file, fallback) {
  try {
    return JSON.parse(fs.readFileSync(file, 'utf8'));
  } catch (error) {
    if (error.code === 'ENOENT') {
      return fallback;
    }
    throw error;
  }
}

function writeFileAtomic(file, content) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  const tmp = `${file}.${process.pid}.tmp`;
  fs.writeFileSync(tmp, content);
  fs.renameSync(tmp, file);
}

function writeJsonAtomic(file, value) {
  writeFileAtomic(file, `${JSON.stringify(value, null, 2)}\n`);
}

function required(name) {
  const value = options[name];
  if (!value) {
    throw new Error(`missing --${name}`);
  }
  return value;
}

function numberOption(name) {
  const value = options[name];
  if (value === undefined || value === '') {
    return undefined;
  }
  const parsed = Number(value);
  if (!Number.isInteger(parsed) || parsed < 1) {
    throw new Error(`invalid --${name}: ${value}`);
  }
  return parsed;
}

function buildPorts() {
  return cleanObject({
    pg: numberOption('pg-port'),
    redis: numberOption('redis-port'),
    be: numberOption('be-port'),
    vite: numberOption('vite-port'),
    bff: numberOption('bff-port'),
    minioApi: numberOption('minio-api-port'),
    minioConsole: numberOption('minio-console-port'),
    prodFrontend: numberOption('prod-frontend-port'),
  });
}

function cleanObject(value) {
  return Object.fromEntries(
    Object.entries(value).filter(([, item]) => item !== undefined && item !== ''),
  );
}

function activeStatus(status) {
  return !['archived', 'deleted'].includes(String(status || '').toLowerCase());
}

function findPortConflict(registry, slug, ports) {
  for (const [otherSlug, entry] of Object.entries(registry.envs || {})) {
    if (otherSlug === slug || !activeStatus(entry.status)) {
      continue;
    }
    const otherPorts = entry.ports || {};
    for (const [name, port] of Object.entries(ports)) {
      if (port !== undefined && Number(otherPorts[name]) === Number(port)) {
        return {
          slug: otherSlug,
          name,
          port,
        };
      }
    }
  }
  return null;
}

function assertPortsFree(root, slug, ports) {
  const registry = readJson(registryPath(root), { version: 1, envs: {} });
  const conflict = findPortConflict(registry, slug, ports);
  if (conflict) {
    console.error(
      `port conflict: ${conflict.name} port ${conflict.port} is already registered by ${conflict.slug}`,
    );
    process.exit(3);
  }
}

function shellQuote(value) {
  return `'${String(value).replaceAll("'", "'\\''")}'`;
}

function shellAssign(name, value) {
  if (value === undefined || value === '') {
    return '';
  }
  return `export ${name}=${shellQuote(value)}`;
}

function buildExports(manifest) {
  const lines = [
    `# Generated by scripts/dev/lib/env-registry.mjs on ${manifest.updatedAt}`,
    shellAssign('AURA_ENV_REGISTRY_ROOT', manifest.registryRoot),
    shellAssign('AURA_ENV_ROOT', manifest.envRoot),
    shellAssign('AURA_ENV_AUTH_ROOT', manifest.authRoot),
    shellAssign('AURA_ENV_SLUG', manifest.slug),
    shellAssign('AURA_ENV_PROFILE', 'r2'),
    shellAssign('COMPOSE_PROJECT_NAME', manifest.composeProject),
    shellAssign('STACK_MODE', 'infra'),
    shellAssign('OFFSET', manifest.offset),
    shellAssign('PG_HOST', 'localhost'),
    shellAssign('PG_PORT', manifest.ports.pg),
    shellAssign('PG_USER', 'auraboot'),
    shellAssign('PG_DB', 'aura_boot'),
    shellAssign('PGPASSWORD', 'auraboot_dev'),
    shellAssign('BE_PORT', manifest.ports.be),
    shellAssign('VITE_PORT', manifest.ports.vite),
    shellAssign('BFF_PORT', manifest.ports.bff),
    shellAssign('REDIS_PORT', manifest.ports.redis),
    shellAssign('MINIO_API_PORT', manifest.ports.minioApi),
    shellAssign('MINIO_CONSOLE_PORT', manifest.ports.minioConsole),
    shellAssign('PROD_FRONTEND_PORT', manifest.ports.prodFrontend),
    shellAssign('BACKEND_URL', manifest.urls.backend),
    shellAssign('PLAYWRIGHT_BASE_URL', manifest.urls.base),
    shellAssign('BFF_URL', manifest.urls.bff),
    shellAssign('PW_STORAGE_DIR', manifest.authRoot),
    shellAssign('PW_ADMIN_STORAGE_STATE', path.join(manifest.authRoot, 'admin.json')),
    shellAssign('PW_OPERATOR_STORAGE_STATE', path.join(manifest.authRoot, 'operator.json')),
    shellAssign('PW_VIEWER_STORAGE_STATE', path.join(manifest.authRoot, 'viewer.json')),
  ].filter(Boolean);
  return `${lines.join('\n')}\n`;
}

function upsert() {
  const root = registryRoot();
  const slug = required('slug');
  const ports = buildPorts();
  assertPortsFree(root, slug, ports);

  const rootForEnv = envRoot(root, slug);
  const authRoot = options['auth-root'] || path.join(rootForEnv, 'auth');
  const manifestPath = path.join(rootForEnv, 'manifest.json');
  const exportsPath = path.join(rootForEnv, 'exports.env');
  const registryFile = registryPath(root);
  const registry = readJson(registryFile, { version: 1, envs: {} });
  const existingManifest = readJson(manifestPath, {});
  const now = new Date().toISOString();

  const manifest = cleanObject({
    version: 1,
    slug,
    mode: options.mode || 'bugfix',
    product: options.product || 'oss',
    coreRoot: options['core-root'],
    enterpriseRoot: options['enterprise-root'],
    coreBranch: options['core-branch'],
    enterpriseBranch: options['enterprise-branch'],
    composeProject: options['compose-project'] || `auraboot-${slug}`,
    offset: numberOption('offset'),
    stackEnvFile: options['stack-env-file'],
    status: options.status || 'running',
    ports,
    urls: cleanObject({
      backend: options['backend-url'] || (ports.be ? `http://localhost:${ports.be}` : undefined),
      base: options['base-url'] || (ports.vite ? `http://localhost:${ports.vite}` : undefined),
      bff: options['bff-url'] || (ports.bff ? `http://localhost:${ports.bff}` : undefined),
    }),
    registryRoot: root,
    envRoot: rootForEnv,
    authRoot,
    manifestPath,
    exportsPath,
    createdAt: existingManifest.createdAt || now,
    updatedAt: now,
  });

  fs.mkdirSync(authRoot, { recursive: true });
  writeJsonAtomic(manifestPath, manifest);
  writeFileAtomic(exportsPath, buildExports(manifest));

  registry.version = 1;
  registry.updatedAt = now;
  registry.envs = registry.envs || {};
  registry.envs[slug] = cleanObject({
    slug,
    mode: manifest.mode,
    product: manifest.product,
    status: manifest.status,
    coreRoot: manifest.coreRoot,
    enterpriseRoot: manifest.enterpriseRoot,
    coreBranch: manifest.coreBranch,
    enterpriseBranch: manifest.enterpriseBranch,
    composeProject: manifest.composeProject,
    ports: manifest.ports,
    authRoot: manifest.authRoot,
    manifestPath,
    exportsPath,
    updatedAt: now,
  });
  writeJsonAtomic(registryFile, registry);

  console.log(`registered env: ${slug}`);
  console.log(`manifest: ${manifestPath}`);
  console.log(`auth: ${authRoot}`);
  console.log(`exports: ${exportsPath}`);
}

function list() {
  const root = registryRoot();
  const registry = readJson(registryPath(root), { version: 1, envs: {} });
  const entries = Object.values(registry.envs || {}).sort((a, b) => a.slug.localeCompare(b.slug));
  if (entries.length === 0) {
    console.log('(no registered environments)');
    return;
  }
  for (const entry of entries) {
    const ports = entry.ports || {};
    console.log(
      [
        entry.slug,
        entry.status || 'unknown',
        entry.product || 'unknown',
        `branch=${entry.coreBranch || 'unknown'}`,
        `pg=${ports.pg || ''}`,
        `be=${ports.be || ''}`,
        `vite=${ports.vite || ''}`,
        `bff=${ports.bff || ''}`,
        `auth=${entry.authRoot || ''}`,
      ].join('\t'),
    );
  }
}

function inspect() {
  const root = registryRoot();
  const slug = required('slug');
  const registry = readJson(registryPath(root), { version: 1, envs: {} });
  const entry = registry.envs?.[slug];
  if (!entry) {
    console.error(`environment not registered: ${slug}`);
    process.exit(1);
  }
  const manifest = readJson(entry.manifestPath, entry);
  console.log(JSON.stringify(manifest));
}

try {
  switch (command) {
    case 'upsert':
      upsert();
      break;
    case 'list':
      list();
      break;
    case 'inspect':
      inspect();
      break;
    case 'assert-ports-free':
      assertPortsFree(registryRoot(), required('slug'), buildPorts());
      break;
    case 'help':
    case '--help':
    case '-h':
      usage();
      break;
    default:
      console.error(`unknown command: ${command}`);
      usage();
      process.exit(2);
  }
} catch (error) {
  console.error(`ERROR: ${error.message}`);
  process.exit(2);
}
