#!/usr/bin/env node
/**
 * sync-dsl-action-catalog.mjs — the machine-readable contract for DSL button actions.
 *
 * Why this exists (2026-07-15, conversation → CS 坐席台 / ENT#784):
 *
 * A workbench button was written `onClick: { action: { type: "api", endpoint, body } }`.
 * There is no `api` action, and workbench actions are not object-shaped — the runtime only
 * recognizes `state.set | dataSource.reload | navigate | command.execute` as STRING verbs.
 * The button did nothing: DSL validation was green, the page rendered, the import validator
 * returned success, and there was zero runtime signal (the executor silently no-op'd).
 * Two guesses caused it: `type:"api"` (there IS an `api.request` verb — but only in a DIFFERENT
 * dialect, ActionRegistry, and object-shaped) and, once fixed, `args.refresh` for the reload
 * key (the workbench key is `args.reload`; `refresh` is again a verb in the OTHER dialect).
 *
 * Three dispatchers coexist, each with its own vocabulary and overlapping-but-different words —
 * so pattern-matching from one onto another produces plausible-looking, silently-dead config:
 *   1. workbench-action-bar  → executeSimpleWorkbenchAction  (string verb + args; reload key = `reload`)
 *   2. table rowActions      → TableBlockRenderer            (object { type, ... }; reload key = `refresh`)
 *   3. form / generic buttons→ ActionRegistry                (object { type, ... }; has `api.request`, `refresh`)
 *
 * This script parses the runtime source (never a hand-maintained list, so it cannot drift away
 * from what actually runs) and emits plugins/schemas/dsl-action-catalog.json. That file is:
 *   - the query surface an agent reads BEFORE writing a button (so it does not guess), and
 *   - the legal set the gate scripts/check-dsl-actions.mjs validates plugin DSL against.
 *
 * Same philosophy as check-dsl-render-types.mjs: one source of truth, read from the code.
 *
 * Usage:
 *   node scripts/sync-dsl-action-catalog.mjs            # regenerate the catalog file
 *   node scripts/sync-dsl-action-catalog.mjs --check    # fail if the committed file is stale
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const WORKBENCH_SRC = path.join(
  REPO_ROOT,
  'web-admin/app/framework/meta/rendering/blocks/workbenchBlockUtils.ts',
);
const ACTION_REGISTRY_SRC = path.join(
  REPO_ROOT,
  'web-admin/app/framework/meta/runtime/actions/ActionRegistry.ts',
);
const OUT = path.join(REPO_ROOT, 'plugins/schemas/dsl-action-catalog.json');

/**
 * Workbench verbs: read straight from the `if (config.action === '<verb>')` branches.
 * The lookbehind skips `typeof config.action === 'object'` (a shape check, not a verb branch).
 */
export function extractWorkbenchVerbs(src) {
  const verbs = [...src.matchAll(/(?<!typeof )config\.action === '([a-zA-Z.]+)'/g)].map((m) => m[1]);
  return [...new Set(verbs)].sort();
}

/**
 * Every `args.<key>` / `args?.<key>` the workbench executor actually reads. This IS the set of
 * arg keys the runtime honors — any key not here is silently ignored (that is what happened to
 * `refresh`), so flagging an unknown key is a true positive, not a guess.
 */
export function extractWorkbenchArgKeys(src) {
  const keys = [...src.matchAll(/\bargs\??\.([a-zA-Z][a-zA-Z0-9]*)/g)].map((m) => m[1]);
  return [...new Set(keys)].sort();
}

/** ActionRegistry verbs: `actionRegistry.register('<verb>' ...)`, incl. the multiline form. */
export function extractRegistryVerbs(src) {
  const verbs = [...src.matchAll(/actionRegistry\.register\(\s*'([a-zA-Z.]+)'/g)].map((m) => m[1]);
  return [...new Set(verbs)].sort();
}

export function buildCatalog() {
  const wbSrc = fs.readFileSync(WORKBENCH_SRC, 'utf8');
  const arSrc = fs.readFileSync(ACTION_REGISTRY_SRC, 'utf8');

  const workbenchVerbs = extractWorkbenchVerbs(wbSrc);
  const workbenchArgKeys = extractWorkbenchArgKeys(wbSrc);
  const registryVerbs = extractRegistryVerbs(arSrc);

  if (workbenchVerbs.length === 0 || registryVerbs.length === 0 || workbenchArgKeys.length === 0) {
    console.error(
      'FATAL: parsed 0 workbench verbs / 0 registry verbs / 0 arg keys. The source shape changed —\n' +
        'fix the extractors in this script rather than shipping an empty catalog, or the gate and\n' +
        'the agent query surface both go vacuous.',
    );
    process.exit(2);
  }

  // Deterministic (sorted, no timestamp) so `--check` can deep-compare without spurious diffs.
  return {
    version: '1.0',
    $generated:
      'GENERATED by scripts/sync-dsl-action-catalog.mjs from runtime source. Do not hand-edit. ' +
      'Regenerate after changing workbenchBlockUtils.ts or ActionRegistry.ts.',
    generatedFrom: [
      'web-admin/app/framework/meta/rendering/blocks/workbenchBlockUtils.ts',
      'web-admin/app/framework/meta/runtime/actions/ActionRegistry.ts',
    ],
    surfaces: {
      'workbench-action-bar': {
        usedBy: "block.blockType == 'workbench-action-bar', each actions[].onClick",
        dispatcher: 'workbenchBlockUtils.executeSimpleWorkbenchAction',
        actionShape: 'string',
        gated: true,
        example: {
          action: 'command.execute',
          args: {
            command: 'cs:go_online',
            targetRecordPid: '${state.agent.pid}',
            reload: ['agentStatus'],
          },
        },
        verbs: workbenchVerbs,
        reloadKey: 'reload',
        recognizedArgKeys: workbenchArgKeys,
        notes: [
          "action is a STRING verb, never an object. There is NO 'api' verb — a backend write is command.execute.",
          "the reload key is 'reload' (args.reload); 'refresh' is NOT read here — it is a verb name in the other two dialects.",
          'unknown verbs and unknown top-level command.execute arg keys throw at runtime and fail scripts/check-dsl-actions.mjs.',
        ],
      },
      'table-row-actions': {
        usedBy: "block table rowActions[].action",
        dispatcher: 'TableBlockRenderer (a SEPARATE dispatcher from ActionRegistry)',
        actionShape: 'object',
        gated: false,
        example: { type: 'navigate', to: '/p/model_code/view/{pid}' },
        confirmedTypes: ['navigate', 'command'],
        notes: [
          "row action is object-shaped { type, ... } — the OPPOSITE of workbench's string verb.",
          "the reload key here is 'refresh' (NOT 'reload') — inverse of workbench. Confirm against TableBlockRenderer before use.",
        ],
      },
      'action-registry': {
        usedBy: 'form / filter / generic buttons resolved through ActionRegistry',
        dispatcher: 'ActionRegistry',
        actionShape: 'object',
        gated: false,
        example: { type: 'api.request', args: { endpoint: '/api/...', method: 'post' } },
        verbs: registryVerbs,
        notes: [
          "'api.request' exists HERE (object-shaped) but NOT in the workbench dialect — do not port it to a workbench onClick.",
          "'refresh' is a verb here; in the workbench dialect the equivalent is the arg key args.reload.",
        ],
      },
    },
  };
}

function main() {
  const check = process.argv.includes('--check');
  const catalog = buildCatalog();
  const serialized = `${JSON.stringify(catalog, null, 2)}\n`;

  if (check) {
    if (!fs.existsSync(OUT)) {
      console.error(`STALE: ${path.relative(REPO_ROOT, OUT)} is missing. Run:\n  node scripts/sync-dsl-action-catalog.mjs`);
      process.exit(1);
    }
    const committed = fs.readFileSync(OUT, 'utf8');
    if (committed !== serialized) {
      console.error(
        `STALE: ${path.relative(REPO_ROOT, OUT)} does not match the runtime source.\n` +
          'Regenerate it:\n  node scripts/sync-dsl-action-catalog.mjs',
      );
      process.exit(1);
    }
    console.log(`OK: ${path.relative(REPO_ROOT, OUT)} is up to date.`);
    return;
  }

  fs.writeFileSync(OUT, serialized);
  console.log(`Wrote ${path.relative(REPO_ROOT, OUT)}`);
  console.log(`  workbench verbs : ${catalog.surfaces['workbench-action-bar'].verbs.join(', ')}`);
  console.log(`  registry verbs  : ${catalog.surfaces['action-registry'].verbs.length}`);
}

// Only run when invoked directly, so check-dsl-actions.mjs can import the extractors.
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
