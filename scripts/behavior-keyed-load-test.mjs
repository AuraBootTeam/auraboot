#!/usr/bin/env node
import { execFileSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

const args = parseArgs(process.argv.slice(2));
if (args.help) {
  printHelp();
  process.exit(0);
}

const backendUrl = args.backendUrl ?? process.env.BACKEND_URL;
if (!backendUrl) {
  fail('BACKEND_URL is required. Run: eval "$(./scripts/oss-golden-stack.sh env <runtime>)"');
}

const batches = numberArg(args.batches, 10);
const batchSize = numberArg(args.batchSize, 50);
const concurrency = numberArg(args.concurrency, 4);
const p95BudgetMs = numberArg(args.p95BudgetMs, 1500);
const pollTimeoutMs = numberArg(args.pollTimeoutMs, 30000);
const tenantId = numberArg(args.tenantId, 990_606);
const runId = args.runId ?? `bkf6-${Date.now().toString(36)}`;
const siteKey = args.siteKey ?? `abk_${runId.replace(/[^0-9A-Za-z]/g, '')}${'0'.repeat(32)}`.slice(0, 48);

if (batchSize > 50) {
  fail(`batchSize=${batchSize} exceeds behavior.collect.keyed.max-batch default 50`);
}
if (batches <= 0 || batchSize <= 0 || concurrency <= 0) {
  fail('batches, batch-size, and concurrency must be positive');
}

const expectedEvents = batches * batchSize;

ensureSchema();
seedSiteKey();
cleanupRunEvents();

const timings = [];
let accepted = 0;
let failedRequests = 0;

const queue = Array.from({ length: batches }, (_, i) => i);
await Promise.all(Array.from({ length: Math.min(concurrency, batches) }, async () => {
  while (queue.length > 0) {
    const batchIndex = queue.shift();
    const result = await postBatch(batchIndex);
    timings.push(result.ms);
    accepted += result.accepted;
    if (result.status !== 200 || result.accepted !== batchSize) {
      failedRequests++;
    }
  }
}));

const persisted = await pollPersistedCount();
const p95Ms = percentile(timings, 0.95);
const summary = {
  runId,
  tenantId,
  siteKey,
  batches,
  batchSize,
  concurrency,
  expectedEvents,
  accepted,
  persisted,
  droppedGoodEvents: expectedEvents - persisted,
  failedRequests,
  p95Ms,
  p95BudgetMs,
};

console.log(JSON.stringify(summary, null, 2));

if (failedRequests > 0) {
  fail(`failedRequests=${failedRequests}`);
}
if (accepted !== expectedEvents) {
  fail(`accepted=${accepted}, expected=${expectedEvents}`);
}
if (persisted !== expectedEvents) {
  fail(`persisted=${persisted}, expected=${expectedEvents}`);
}
if (p95Ms > p95BudgetMs) {
  fail(`p95Ms=${p95Ms} exceeds budget ${p95BudgetMs}`);
}

function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === '--help' || arg === '-h') {
      out.help = true;
      continue;
    }
    if (!arg.startsWith('--')) {
      fail(`unknown positional argument: ${arg}`);
    }
    const key = arg.slice(2).replace(/-([a-z])/g, (_, c) => c.toUpperCase());
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      fail(`missing value for ${arg}`);
    }
    out[key] = next;
    i++;
  }
  return out;
}

function numberArg(value, fallback) {
  if (value == null) return fallback;
  const n = Number(value);
  if (!Number.isFinite(n)) {
    fail(`invalid number: ${value}`);
  }
  return Math.trunc(n);
}

function printHelp() {
  console.log(`Usage:
  eval "$(./scripts/oss-golden-stack.sh env <runtime>)"
  node scripts/behavior-keyed-load-test.mjs [--batches 10] [--batch-size 50] [--concurrency 4] [--p95-budget-ms 1500]

Environment:
  BACKEND_URL, PGHOST/PGPORT/PGUSER/PGDATABASE/PGPASSWORD from oss-golden-stack env
`);
}

function ensureSchema() {
  runPsqlFile('platform/src/main/resources/db/migration/core/V20260620000200__behavior_event_store.sql');
  runPsqlFile('platform/src/main/resources/db/migration/core/V20260622000000__behavior_quarantine_sink.sql');
  runPsqlFile('platform/src/main/resources/db/migration/core/V20260622001000__behavior_quarantine_replay_state.sql');
  psql(`
    CREATE TABLE IF NOT EXISTS mt_behavior_site_key (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      pid VARCHAR(32) NOT NULL,
      tenant_id BIGINT,
      site_key VARCHAR(64),
      name VARCHAR(200) NOT NULL,
      status VARCHAR(32) DEFAULT 'active',
      origin_allowlist JSONB,
      created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
      created_by VARCHAR(255),
      updated_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
      updated_by VARCHAR(255)
    )
  `);
  psql('CREATE UNIQUE INDEX IF NOT EXISTS uk_mt_behavior_site_key_site_key ON mt_behavior_site_key (site_key)');
}

function seedSiteKey() {
  const pid = sqlLit(`bkf6${runId.replace(/[^0-9A-Za-z]/g, '').slice(0, 24)}`);
  psql(`DELETE FROM mt_behavior_site_key WHERE site_key='${sqlLit(siteKey)}' OR pid='${pid}'`);
  psql(`
    INSERT INTO mt_behavior_site_key (pid, tenant_id, site_key, name, status, origin_allowlist)
    VALUES ('${pid}', ${tenantId}, '${sqlLit(siteKey)}', 'BK-F6 keyed load ${sqlLit(runId)}', 'active', NULL)
  `);
}

function cleanupRunEvents() {
  psql(`DELETE FROM ab_behavior_event WHERE tenant_id=${tenantId} AND event_id LIKE '${sqlLit(runId)}-%'`);
  psql(`DELETE FROM ab_behavior_quarantine WHERE tenant_id=${tenantId} AND event_id LIKE '${sqlLit(runId)}-%'`);
}

async function postBatch(batchIndex) {
  const start = performance.now();
  const events = Array.from({ length: batchSize }, (_, i) => event(batchIndex * batchSize + i));
  const response = await fetch(`${backendUrl}/api/collect/keyed`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'X-Site-Key': siteKey,
      Origin: 'https://bkf6-load.example.com',
      'X-Forwarded-For': `10.66.6.${(batchIndex % 200) + 1}`,
    },
    body: JSON.stringify({ events }),
  });
  const text = await response.text();
  const ms = Math.round(performance.now() - start);
  let body = {};
  try {
    body = JSON.parse(text);
  } catch {
    // Keep body as {} and let failedRequests catch it.
  }
  if (response.status !== 200) {
    console.error(`request ${batchIndex} failed: status=${response.status} body=${text}`);
  }
  return { status: response.status, accepted: Number(body.accepted ?? 0), ms };
}

function event(index) {
  return {
    eventId: `${runId}-${index.toString(36)}`,
    schemaVersion: '1',
    eventName: 'page_view',
    eventCategory: 'navigation',
    source: 'web',
    occurredAt: new Date().toISOString(),
    anonId: `bkf6-anon-${index % 100}`,
    clientSessionId: `bkf6-session-${Math.floor(index / batchSize)}`,
    producerName: 'behavior-keyed-load-test',
    producerVersion: '1.0.0',
    props: { routeTemplate: '/bkf6/load', index: String(index) },
  };
}

async function pollPersistedCount() {
  const deadline = Date.now() + pollTimeoutMs;
  let last = 0;
  while (Date.now() <= deadline) {
    last = Number(psql(`SELECT count(1) FROM ab_behavior_event WHERE tenant_id=${tenantId} AND event_id LIKE '${sqlLit(runId)}-%'`));
    if (last === expectedEvents) {
      return last;
    }
    await new Promise((resolve) => setTimeout(resolve, 500));
  }
  return last;
}

function percentile(values, p) {
  if (values.length === 0) return 0;
  const sorted = [...values].sort((a, b) => a - b);
  const index = Math.max(0, Math.ceil(sorted.length * p) - 1);
  return sorted[index];
}

function runPsqlFile(relative) {
  execFileSync('psql', [...psqlArgs(), '-q', '-f', path.join(repoRoot, relative)], { env: psqlEnv(), stdio: 'pipe' });
}

function psql(sql) {
  return execFileSync('psql', [...psqlArgs(), '-t', '-A', '-P', 'pager=off', '-c', sql], {
    env: psqlEnv(),
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
  }).trim();
}

function psqlArgs() {
  const env = process.env;
  return [
    '-h', env.PGHOST ?? env.PG_HOST ?? '127.0.0.1',
    '-p', env.PGPORT ?? env.PG_PORT ?? '5432',
    '-U', env.PGUSER ?? env.PG_USER ?? 'auraboot',
    '-d', env.PGDATABASE ?? env.PG_DB ?? 'aura_boot',
  ];
}

function psqlEnv() {
  return {
    ...process.env,
    PGPASSWORD: process.env.PGPASSWORD ?? process.env.PG_PASSWORD ?? 'auraboot',
  };
}

function sqlLit(value) {
  return String(value).replace(/'/g, "''");
}

function fail(message) {
  console.error(`[behavior-keyed-load-test] ${message}`);
  process.exit(1);
}
