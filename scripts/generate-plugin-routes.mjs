#!/usr/bin/env node
// scripts/generate-plugin-routes.mjs
// Reads platform plugin routes.json files and generates route config

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const REPO_ROOT = path.resolve(__dirname, '..');
const PLUGINS_DIR = path.join(REPO_ROOT, 'plugins/platform');
const ROUTES_FILE = path.join(REPO_ROOT, 'web-admin/app/routes.ts');
const PUBLIC_ROUTES_FILE = path.join(REPO_ROOT, 'web-admin/app/plugins/_public-routes.ts');

// Scan installed platform plugins
function scanPlugins() {
  const results = [];

  if (!fs.existsSync(PLUGINS_DIR)) return results;

  for (const dir of fs.readdirSync(PLUGINS_DIR)) {
    const pluginDir = path.join(PLUGINS_DIR, dir);
    if (!fs.statSync(pluginDir).isDirectory()) continue;

    const manifestPath = path.join(pluginDir, 'plugin.json');
    const routesPath = path.join(pluginDir, 'config/routes.json');

    if (!fs.existsSync(manifestPath)) continue;
    if (!fs.existsSync(routesPath)) continue;

    const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf-8'));
    if (manifest.type !== 'platform') continue;

    const routesConfig = JSON.parse(fs.readFileSync(routesPath, 'utf-8'));
    const pluginName = manifest.id.split('.').pop() || dir;

    results.push({
      id: manifest.id,
      name: pluginName,
      routesConfig,
      rendering: manifest.rendering || 'csr',
    });
  }

  return results;
}

// Generate route entries for routes.ts
function generateRouteEntries(plugins) {
  const lines = [];

  for (const plugin of plugins) {
    const { name, routesConfig, rendering, id } = plugin;
    const prefix = `./plugins/${name}`;

    lines.push(`    // Plugin: ${id} (${rendering.toUpperCase()})`);

    if (routesConfig.layout) {
      const layoutPath = `${prefix}/${routesConfig.layout.component}.tsx`;
      const idArg = routesConfig.layout.id ? `, { id: "${routesConfig.layout.id}" }` : '';
      lines.push(`    layout("${layoutPath}"${idArg}, [`);

      for (const route of routesConfig.routes) {
        const componentPath = `${prefix}/${route.component}.tsx`;
        if (route.index) {
          lines.push(`        index("${componentPath}"),`);
        } else {
          lines.push(`        route("${route.path}", "${componentPath}"),`);
        }
      }

      lines.push(`    ]),`);
    } else {
      for (const route of routesConfig.routes) {
        const componentPath = `${prefix}/${route.component}.tsx`;
        if (route.index) {
          lines.push(`    index("${componentPath}"),`);
        } else {
          lines.push(`    route("${route.path}", "${componentPath}"),`);
        }
      }
    }

    lines.push('');
  }

  return lines.join('\n');
}

// Generate public routes file
function generatePublicRoutes(plugins) {
  const publicPaths = [];

  for (const plugin of plugins) {
    if (!plugin.routesConfig.publicRoutes) continue;
    for (const route of plugin.routesConfig.routes) {
      if (route.index) continue;
      const routePath = route.path.startsWith('/') ? route.path : `/${route.path}`;
      // Clean up: remove trailing wildcards and params for prefix matching
      const cleanPath = routePath.replace(/\/\*$/, '').replace(/\/:[^/]+$/, '');
      if (cleanPath && cleanPath !== '/') {
        publicPaths.push(cleanPath);
      }
    }
    // If plugin has an index route, add '/'
    if (plugin.routesConfig.routes.some(r => r.index)) {
      publicPaths.push('/');
    }
  }

  // Deduplicate
  const unique = [...new Set(publicPaths)].sort();

  const lines = [
    '// AUTO-GENERATED by scripts/generate-plugin-routes.mjs',
    '// DO NOT EDIT MANUALLY',
    '',
    `export const PLUGIN_PUBLIC_ROUTES: string[] = [`,
    ...unique.map(p => `  '${p}',`),
    '];',
    '',
  ];

  return lines.join('\n');
}

// Update routes.ts — replace content between markers.
//
// Header format (single canonical block, idempotent on re-runs):
//
//     // >>> AUTO-GENERATED: Platform Plugin Routes (DO NOT EDIT) >>>
//     // Managed by scripts/generate-plugin-routes.mjs
//     <generated route entries>
//     // <<< END AUTO-GENERATED <<<
//
// The previous implementation used the same `══...` separator string as both
// the opening and closing of the start block AND as the closing line of the
// end block. `indexOf` therefore matched the first separator anywhere in the
// file, which made the markers vulnerable to accidental matches when route
// entries (or unrelated comments) reused the separator glyphs. The replacement
// uses two distinct, directional sentinels (`>>> ... >>>` for begin,
// `<<< ... <<<` for end) so each marker appears exactly once and matches
// unambiguously.
function updateRoutesFile(routeEntries) {
  const BEGIN_MARKER = '    // >>> AUTO-GENERATED: Platform Plugin Routes (DO NOT EDIT) >>>';
  const BEGIN_NOTE   = '    // Managed by scripts/generate-plugin-routes.mjs';
  const END_MARKER   = '    // <<< END AUTO-GENERATED <<<';

  // Legacy markers from the previous header format — still recognised so that
  // the first run after this change rewrites the block in place instead of
  // appending a second one.
  const LEGACY_LABEL    = '    // AUTO-GENERATED: Platform Plugin Routes';
  const LEGACY_END_TAG  = '    // END AUTO-GENERATED';
  const LEGACY_SEP      = '    // ══════════════════════════════════════════════';

  let content = fs.readFileSync(ROUTES_FILE, 'utf-8');

  const startBlock = `${BEGIN_MARKER}\n${BEGIN_NOTE}\n`;
  const endBlock = `${END_MARKER}\n`;

  const hasCanonical = content.includes(BEGIN_MARKER) && content.includes(END_MARKER);
  const hasLegacy = !hasCanonical && content.includes(LEGACY_LABEL);

  if (hasCanonical) {
    // Replace existing canonical block.
    const startIdx = content.indexOf(BEGIN_MARKER);
    const endIdx = content.indexOf(END_MARKER, startIdx);
    if (startIdx === -1 || endIdx === -1) {
      console.error('Canonical markers detected but could not be located cleanly.');
      process.exit(1);
    }
    const endLineEnd = content.indexOf('\n', endIdx);
    const before = content.substring(0, startIdx);
    const after = content.substring(endLineEnd !== -1 ? endLineEnd + 1 : content.length);
    content = before + startBlock + routeEntries + endBlock + after;
  } else if (hasLegacy) {
    // Migrate from legacy format: locate the first legacy separator that
    // immediately precedes the legacy label, find the legacy end, and replace
    // the whole region with the canonical block.
    const labelIdx = content.indexOf(LEGACY_LABEL);
    // Walk back to the separator line that opens the legacy block.
    const sepIdx = content.lastIndexOf(LEGACY_SEP, labelIdx);
    const startIdx = sepIdx !== -1 ? sepIdx : labelIdx;
    const endTagIdx = content.indexOf(LEGACY_END_TAG, labelIdx);
    if (endTagIdx === -1) {
      console.error('Legacy AUTO-GENERATED block found but END marker missing.');
      process.exit(1);
    }
    // Legacy end was followed by another separator line; consume it too.
    const afterEndTag = content.indexOf('\n', endTagIdx) + 1;
    const trailingSepIdx = content.indexOf(LEGACY_SEP, afterEndTag);
    let endLineEnd;
    if (trailingSepIdx !== -1 && trailingSepIdx < afterEndTag + 200) {
      endLineEnd = content.indexOf('\n', trailingSepIdx);
    } else {
      endLineEnd = afterEndTag - 1;
    }
    const before = content.substring(0, startIdx);
    const after = content.substring(endLineEnd !== -1 ? endLineEnd + 1 : content.length);
    content = before + startBlock + routeEntries + endBlock + after;
  } else {
    // Fresh insertion: place the block right after `export default [`.
    const exportIdx = content.indexOf('export default [');
    if (exportIdx === -1) {
      console.error('Could not find "export default [" in routes.ts');
      process.exit(1);
    }
    const insertIdx = content.indexOf('\n', exportIdx) + 1;
    const before = content.substring(0, insertIdx);
    const after = content.substring(insertIdx);
    content = before + startBlock + routeEntries + endBlock + '\n' + after;
  }

  fs.writeFileSync(ROUTES_FILE, content, 'utf-8');
}

// Main
function main() {
  console.log('Generating platform plugin routes...');

  const plugins = scanPlugins();
  console.log(`Found ${plugins.length} platform plugin(s)`);

  // ALWAYS write _public-routes.ts (even with an empty list). The file is
  // gitignored, but `app/middleware/sessionMiddlewareFactory.ts` imports
  // it unconditionally — a fresh checkout with zero platform plugins must
  // still get a valid stub or the dev server fails on every SSR request
  // with "Cannot find module '~/plugins/_public-routes'".
  const publicRoutes = generatePublicRoutes(plugins);
  const publicRoutesDir = path.dirname(PUBLIC_ROUTES_FILE);
  if (!fs.existsSync(publicRoutesDir)) fs.mkdirSync(publicRoutesDir, { recursive: true });
  fs.writeFileSync(PUBLIC_ROUTES_FILE, publicRoutes, 'utf-8');
  console.log(`Generated ${PUBLIC_ROUTES_FILE}`);

  if (plugins.length === 0) {
    console.log('No platform plugins found. Wrote empty PLUGIN_PUBLIC_ROUTES stub.');
    return;
  }

  // Route entries are now managed by packages/website/route-manifest.ts
  // No longer injecting into routes.ts (Route Manifest architecture)
  console.log('Skipping routes.ts update (managed by Route Manifest architecture)');

  console.log('Done.');
}

main();
