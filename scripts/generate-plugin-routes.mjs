#!/usr/bin/env node
// scripts/generate-plugin-routes.mjs
// Reads platform plugin routes.json files and generates route config

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const REPO_ROOT = path.resolve(__dirname, '..');
const PLUGINS_DIR = path.join(REPO_ROOT, 'plugins/platform');
const ROUTES_FILE = path.join(REPO_ROOT, 'web-admin/app/routes.ts');
const PUBLIC_ROUTES_FILE = path.join(REPO_ROOT, 'web-admin/app/plugins/_public-routes.ts');

// Scan installed platform plugins
function scanPlugins() {
  const results = [];

  if (!fs.existsSync(PLUGINS_DIR)) return results;

  for (const dir of fs.readdirSync(PLUGINS_DIR)) {
    const pluginDir = path.join(PLUGINS_DIR, dir);
    if (!fs.statSync(pluginDir).isDirectory()) continue;

    const manifestPath = path.join(pluginDir, 'plugin.json');
    const routesPath = path.join(pluginDir, 'config/routes.json');

    if (!fs.existsSync(manifestPath)) continue;
    if (!fs.existsSync(routesPath)) continue;

    const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf-8'));
    if (manifest.type !== 'platform') continue;

    const routesConfig = JSON.parse(fs.readFileSync(routesPath, 'utf-8'));
    const pluginName = manifest.id.split('.').pop() || dir;

    results.push({
      id: manifest.id,
      name: pluginName,
      routesConfig,
      rendering: manifest.rendering || 'csr',
    });
  }

  return results;
}

// Generate route entries for routes.ts
function generateRouteEntries(plugins) {
  const lines = [];

  for (const plugin of plugins) {
    const { name, routesConfig, rendering, id } = plugin;
    const prefix = `./plugins/${name}`;

    lines.push(`    // Plugin: ${id} (${rendering.toUpperCase()})`);

    if (routesConfig.layout) {
      const layoutPath = `${prefix}/${routesConfig.layout.component}.tsx`;
      const idArg = routesConfig.layout.id ? `, { id: "${routesConfig.layout.id}" }` : '';
      lines.push(`    layout("${layoutPath}"${idArg}, [`);

      for (const route of routesConfig.routes) {
        const componentPath = `${prefix}/${route.component}.tsx`;
        if (route.index) {
          lines.push(`        index("${componentPath}"),`);
        } else {
          lines.push(`        route("${route.path}", "${componentPath}"),`);
        }
      }

      lines.push(`    ]),`);
    } else {
      for (const route of routesConfig.routes) {
        const componentPath = `${prefix}/${route.component}.tsx`;
        if (route.index) {
          lines.push(`    index("${componentPath}"),`);
        } else {
          lines.push(`    route("${route.path}", "${componentPath}"),`);
        }
      }
    }

    lines.push('');
  }

  return lines.join('\n');
}

// Generate public routes file
function generatePublicRoutes(plugins) {
  const publicPaths = [];

  for (const plugin of plugins) {
    if (!plugin.routesConfig.publicRoutes) continue;
    for (const route of plugin.routesConfig.routes) {
      if (route.index) continue;
      const routePath = route.path.startsWith('/') ? route.path : `/${route.path}`;
      // Clean up: remove trailing wildcards and params for prefix matching
      const cleanPath = routePath.replace(/\/\*$/, '').replace(/\/:[^/]+$/, '');
      if (cleanPath && cleanPath !== '/') {
        publicPaths.push(cleanPath);
      }
    }
    // If plugin has an index route, add '/'
    if (plugin.routesConfig.routes.some(r => r.index)) {
      publicPaths.push('/');
    }
  }

  // Deduplicate
  const unique = [...new Set(publicPaths)].sort();

  const lines = [
    '// AUTO-GENERATED by scripts/generate-plugin-routes.mjs',
    '// DO NOT EDIT MANUALLY',
    '',
    `export const PLUGIN_PUBLIC_ROUTES: string[] = [`,
    ...unique.map(p => `  '${p}',`),
    '];',
    '',
  ];

  return lines.join('\n');
}

// Update routes.ts — replace content between markers
function updateRoutesFile(routeEntries) {
  const START_MARKER = '    // ══════════════════════════════════════════════';
  const START_LABEL = '    // AUTO-GENERATED: Platform Plugin Routes';
  const START_WARNING = '    // DO NOT EDIT — managed by generate-plugin-routes.mjs';
  const END_MARKER = '    // END AUTO-GENERATED';
  const END_LINE = '    // ══════════════════════════════════════════════';

  let content = fs.readFileSync(ROUTES_FILE, 'utf-8');

  const startBlock = `${START_MARKER}\n${START_LABEL}\n${START_WARNING}\n${START_MARKER}\n\n`;
  const endBlock = `\n${END_MARKER}\n${END_LINE}`;

  // Check if markers already exist
  if (content.includes('AUTO-GENERATED: Platform Plugin Routes')) {
    // Find the start and end markers to replace
    const startIdx = content.indexOf(START_MARKER);
    // Find END AUTO-GENERATED after the start
    const endMarkerIdx = content.indexOf(END_MARKER, startIdx);
    if (startIdx !== -1 && endMarkerIdx !== -1) {
      // Find end of the END_LINE after END_MARKER
      const endLineIdx = content.indexOf('\n', content.indexOf(END_LINE, endMarkerIdx));
      const before = content.substring(0, startIdx);
      const after = content.substring(endLineIdx !== -1 ? endLineIdx + 1 : content.length);
      content = before + startBlock + routeEntries + endBlock + '\n' + after;
    }
  } else {
    // Insert after "export default ["
    const exportIdx = content.indexOf('export default [');
    if (exportIdx === -1) {
      console.error('Could not find "export default [" in routes.ts');
      process.exit(1);
    }
    const insertIdx = content.indexOf('\n', exportIdx) + 1;
    const before = content.substring(0, insertIdx);
    const after = content.substring(insertIdx);
    content = before + startBlock + routeEntries + endBlock + '\n\n' + after;
  }

  fs.writeFileSync(ROUTES_FILE, content, 'utf-8');
}

// Main
function main() {
  console.log('Generating platform plugin routes...');

  const plugins = scanPlugins();
  console.log(`Found ${plugins.length} platform plugin(s)`);

  if (plugins.length === 0) {
    console.log('No platform plugins found. Skipping route generation.');
    return;
  }

  // Generate and write public routes
  const publicRoutes = generatePublicRoutes(plugins);
  const publicRoutesDir = path.dirname(PUBLIC_ROUTES_FILE);
  if (!fs.existsSync(publicRoutesDir)) fs.mkdirSync(publicRoutesDir, { recursive: true });
  fs.writeFileSync(PUBLIC_ROUTES_FILE, publicRoutes, 'utf-8');
  console.log(`Generated ${PUBLIC_ROUTES_FILE}`);

  // Route entries are now managed by packages/website/route-manifest.ts
  // No longer injecting into routes.ts (Route Manifest architecture)
  console.log('Skipping routes.ts update (managed by Route Manifest architecture)');

  console.log('Done.');
}

main();
